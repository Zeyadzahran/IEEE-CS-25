pair<int, int> start(string position) {
    int x = position[0] - 'a';
    int y = position[1] - '1';
    return {x, y};
}


int bfs(pair<int, int> start, pair<int, int> destination) {
    if (start == destination) return 0; 
    
    vector<vector<bool>> vis(8, vector<bool>(8, false));
    queue<pair<int, int>> q;
    q.push(start);
    vis[start.first][start.second] = true;
    
    int moves = 0;
    
    while (!q.empty()) {
        int size = q.size();
        
        for (int i = 0; i < size; i++) {
            pair<int, int> current = q.front();
            q.pop();
            
            for (int j = 0; j < 8; j++) {
                int nx = current.first + dx[j];
                int ny = current.second + dy[j];
                
             
                if (nx >= 0 && nx < 8 && ny >= 0 && ny < 8 && !vis[nx][ny]) {
                    if (nx == destination.first && ny == destination.second) {
                        return moves + 1;
                    }
                    vis[nx][ny] = true;
                    q.push({nx, ny});
                }
            }
        }
        
        moves++;
    }
    
    return -1; 
}

