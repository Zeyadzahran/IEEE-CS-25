struct Cell {
    int x, y;
};

int n, m;
vector<string> a;
vector<vector<int>> mTime, pTime;
vector<vector<Cell>> parent;

bool is_valid(int x, int y) {
    return x >= 0 && x < n && y >= 0 && y < m && a[x][y] != '#';
}

void bfsM(queue<Cell> &q) {
    while (!q.empty()) {
        Cell current = q.front();
        q.pop();

        for (int i = 0; i < 4; i++) {
            int nx = current.x + (i == 0 ? -1 : i == 1 ? 1 : 0);
            int ny = current.y + (i == 2 ? -1 : i == 3 ? 1 : 0);

            if (is_valid(nx, ny) && mTime[nx][ny] == -1) {
                mTime[nx][ny] = mTime[current.x][current.y] + 1;
                q.push({nx, ny});
            }
        }
    }
}

bool bfsP(Cell start, Cell &escape) {
    queue<Cell> q;
    q.push(start);
    pTime[start.x][start.y] = 0;

    while (!q.empty()) {
        Cell current = q.front();
        q.pop();

        if (current.x == 0 || current.y == 0 || current.x == n - 1 || current.y == m - 1) {
            escape = current;
            return true;
        }

        for (int i = 0; i < 4; i++) {
            int nx = current.x + (i == 0 ? -1 : i == 1 ? 1 : 0);
            int ny = current.y + (i == 2 ? -1 : i == 3 ? 1 : 0);

            if (is_valid(nx, ny) && pTime[nx][ny] == -1 && (mTime[nx][ny] == -1 || pTime[current.x][current.y] + 1 < mTime[nx][ny])) {
                pTime[nx][ny] = pTime[current.x][current.y] + 1;
                parent[nx][ny] = current;
                q.push({nx, ny});
            }
        }
    }
    return false;
}

int main() {
    cin >> n >> m;
    a.resize(n);
    mTime.assign(n, vector<int>(m, -1));
    pTime.assign(n, vector<int>(m, -1));
    parent.assign(n, vector<Cell>(m, {-1, -1}));

    Cell start;
    queue<Cell> mq;


    for (int i = 0; i < n; i++) {
        cin >> a[i];
        for (int j = 0; j < m; j++) {
            if (a[i][j] == 'A') {
                start = {i, j};
            } else if (a[i][j] == 'M') {
                mq.push({i, j});
                mTime[i][j] = 0;
            }
        }
    }

    bfsM(mq);

    Cell escape;
    if (!bfsP(start, escape)) {
        cout << "NO" << endl;
        return 0;
    }

    string path;
    Cell current = escape;
    while (!(current.x == start.x && current.y == start.y)) {
        Cell p = parent[current.x][current.y];

        if (p.x == current.x - 1 && p.y == current.y) {
            path += 'D';
        } else if (p.x == current.x + 1 && p.y == current.y) {
            path += 'U';
        } else if (p.x == current.x && p.y == current.y - 1) {
            path += 'R';
        } else if (p.x == current.x && p.y == current.y + 1) {
            path += 'L';
        }

        current = p;
    }

    reverse(path.begin(), path.end());
    cout << "YES" << endl;
    cout << path.length() << endl;
    cout << path << endl;

    return 0;
}
