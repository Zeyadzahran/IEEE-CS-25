const int dy[4] = {0, 0, 1, -1};
 

void dfs(int x, int y, vector<vector<char>>& grid, vector<vector<bool>>& vis, int n, int m) {
    vis[x][y] = true;
    for (int i = 0; i < 4; i++) {
        int nX = x + dx[i];
        int nY = y + dy[i];
        if (nX >= 0 && nX < n && nY >= 0 && nY < m && grid[nX][nY] == '.' && !vis[nX][nY]) {
            dfs(nX, nY, grid, vis, n, m);
        }
    }
}
 

