ll gcd(ll a, ll b)
{
    while(b != 0)
    {
        ll a2 = a; // stores the old version of a
        a = b;
        b = a2%b;
    }
    return a;
}
 
int lcm(int a, int b) {
    return (a / gcd(a, b)) * b;
}
vi DecimalToBinary(int n)
{
    vi Binary;
    while(n > 0)
    {
        Binary.push_back(n & 1);
        n >>= 1;
    }
    reverse(all(Binary));
    return Binary;
}
 
int BinaryToDecimal(vi Binary)
{
    int Decimal = 0;
    for(int i = 0 ; i < Binary.size() ; ++i)
    {       
 
        Decimal += (Binary[i] * (1 << i));
    }
    return Decimal;
}
bool get_bit(ll n, int ind)
{
    ll msk = 1LL << ind;
    return ((msk & n) ? 1 : 0);
}
 
